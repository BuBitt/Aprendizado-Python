"""
O tipo de dado none em python representa o tipo sem tipo, ou poderia ser reconhecido como tipo vazio
porém, porém falar que é um tipo sem tipo é mais apropriado

OBS: O tipo None é SEMPRE especificado com a primeira letra maiúscula.

Certo: None
Errado: none

- Podemos utilizar None quando queremos criar uma variável, e inicializa-la com um tipo sem tipo
  antes de recebermos um valor final.
"""
numeros = None

print(numeros)
print(type(numeros))

numeros = 1.44, 1.34, 5.67
print(numeros)
print(type(numeros))
