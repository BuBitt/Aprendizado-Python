"""
Peça para o usuário escrever 3 valores inteiros e imprima a soma deles.
"""
print('Por favor, escreva 3 valores inteiros:')
num1 = int(input('Digite o primeiro valor: '))
num2 = int(input('Digite o Segundo valor: '))
num3 = int(input('Digite o terceiro valor: '))

numt = num1 + num2 + num3

print(f'A soma dos 3 valores inteiros é {numt}')
