"""
Leia a distância em km ae apresente-a em milhas
"""
# Input distância em km
distkm = float(input('Digite a distância em quilometros: '))

# Processamento conversão de milhas em km
convm = distkm / 1.6

# Output distância convertida em m
print(f'A Distância de {distkm} quilomentros é equivalente a {convm} milhas.')
